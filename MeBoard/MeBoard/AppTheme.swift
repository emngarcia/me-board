import SwiftUI

extension Color {
    // MARK: – Earth-tone palette (natural white + olive/green)

    /// Olive green — primary accent (buttons, links, selections).
    static let earthAccent = Color(red: 0.42, green: 0.50, blue: 0.32)   // olive green

    /// Deep forest — headings or strong foreground elements.
    static let earthBark   = Color(red: 0.26, green: 0.32, blue: 0.22)   // deep forest

    /// Natural white — soft warm background for cards or sections.
    static let earthSand   = Color(red: 0.97, green: 0.96, blue: 0.93)   // natural white/cream

    /// Slightly warmer cream — card / row background to stand out from earthSand.
    static let earthCard   = Color(red: 0.93, green: 0.91, blue: 0.85)   // warm parchment

    /// Sage green — secondary accent for dividers or labels.
    static let earthSage   = Color(red: 0.55, green: 0.63, blue: 0.46)   // sage

    /// Warm stone — neutral secondary text or disabled states.
    static let earthStone  = Color(red: 0.50, green: 0.50, blue: 0.45)   // muted olive-grey
}
